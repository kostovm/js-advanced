function mathOperations(a, b, action) {

    switch (action) {
        case "+": console.log(a + b); break;
        case "-": console.log(a - b); break;
        case "*": console.log(a * b); break;
        case "/": console.log(a / b); break;
        case "%": console.log(a % b); break;
        case "**": console.log(a ** b); break;
    }

}